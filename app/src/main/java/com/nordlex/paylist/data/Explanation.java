        if (code>0 & code< INCOME.length) {
            if (INCOME[code].length()>2) {
                return INCOME[code];
            } else return "No explanation";
        } else if (code>700 & code< OUTCOME.length+700) {
            if (OUTCOME[code-700].length()>2) {
                return OUTCOME[code - 700];
            } else return "No explanation";
        } else return "Code not found";
